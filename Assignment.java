package Operator_Day4.Assignment;

public class Assignment {
    public static void main(String[] args) {
        // Welcome to Assignment Operator

        System.out.println("WELCOME TO ASSIGNMENT OPERATOR: ");
        int myInt = 9;
        System.out.println(myInt);

        // int is a Data type
        // myInt  is an identifier
        // = is an Assignment operator: means its work to switch the value  Right to Left
        // 9 is a literal

         int newInt = myInt;
        System.out.println(newInt);



    }
}
