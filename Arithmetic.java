package Operator_Day4;

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println("Welcome to calculator shop:");

        int a=8;
        int b=9;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println("Complete your Calculation :)");
        double x=5;
        double y=2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println("Complete your Calculation :)");

        float r=5.2f;
        float t=2.3f;
        System.out.println(r+t);
        System.out.println(r-t);
        System.out.println(r*t);
        System.out.println(r/t);
        System.out.println("Complete your Calculation :)");

        /// using a Modules value
        System.out.println("Check the modules value:");
        System.out.println(a%b);
        System.out.println(x%y);
        System.out.println(r%t);
        System.out.println("Complete your Calculation :)");




    }
}
