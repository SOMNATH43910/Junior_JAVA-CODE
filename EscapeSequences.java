package Day3;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello 'Java' im your lover.....:");
        // ab mai java ko double quotes me dikha na chahu
        //to Mujhe use Escape  karna hga ishke liye muje ye code use karna hai
        // { \"java\" } 👇🏻
        System.out.println("Hello \"Java\" im your lover.....:");
        // ab mai chata hu hello ke baad  mere output next line me aye
        // to mujhe ye code use karna hai { \n  means new line } 👇🏻
        System.out.println("Hello \n Java \n im your lover.....:");
        // ab mai cahata hu hu hello ke baad ek bada sa space aye java ke bich me
        // to mai ye code use karunga { \t  means tab }
        System.out.println("Hello \tJava\t im your lover.....:");
        
    }
}
